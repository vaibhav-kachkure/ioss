<p><h3>Program Outline</h3>
<p class="text-justify">Public Health is an interdisciplinary study in the faculty of Social Work & life sciences and focuses mainly on training human resources in the science of public health, with a strong understanding of the determinants of health and the public health system in the country, the community context, and the influences like globalization, urbanization, global and national policies and a strong foundation in research methodology. The other mandate of the course is to undertake relevant research to analyze complex public health issues and work collaboratively with government, communities, academics, etc. for promoting health and preventing disease conditions and provide evidenced based advice for healthy living. The teaching program educates students to integrate the knowledge and skills of public health into careers in a variety of fields. Through interdisciplinary approach and integrating classroom learning, community exposure, academic research, the school prepares students for leadership positions, developing evidence-based solutions for critical health problems. Students develop competencies as public health professionals to actively engage communities to promote and protect health and, reduce health disparities among disadvantaged, underserved and vulnerable populations.</p>
</div>



