<p><h3>Program Outline</h3>
<p class="text-justify">The global definition of the Social Work Profession states that “Social work is a practice-based profession and an academic discipline that promotes social change and development, social cohesion, and the empowerment and liberation of people. Principles of social justice, human rights, collective responsibility and respect for diversities are central to social work. Underpinned by theories of social work, social sciences, humanities and indigenous knowledge, social work engages people and structures to address life challenges and enhance wellbeing”. The core mandates of social work profession comprising of promotion of social change, social development, social cohesion, and the empowerment and liberation of people is strengthened by the interdisciplinary and trans-disciplinary knowledge and practices which are based on scientific evidences and humanistic principles. The professional social work education train the candidates for effective intervention at the points where people have interaction with their environment. The environment includes the various social systems that people are embedded in and the natural, geographic environment, which has a profound influence on the lives of people. The methodology of social work comprising of primary and secondary methods of social work with a wide range of skills, techniques, strategies, principles and activities at various settings and fields enable the social workers for proactive engagement with people and structures ‘to address life challenges and enhance wellbeing’. The professional education in social work equip the students to confidently engage in the practices of counseling, group work, and community work, policy formulation and analysis, advocacy and political interventions.</p>
<p class="text-justify">Bachelor’s Degree program in Social Work enables students to prepare a foundation for a lucrative career in the Social service sector. The Program emphasis on training the students both theoretically and practically aiming at understanding the social issues and social problems and acquiring the skill of interventions. The program trains students to inculcate the accurate knowledge and methods of intervening into the various social issues and problems, and offering them attractive jobs in Governmental, Non-Governmental, private and corporate sectors.</p>

