<p><h3>Program Outline</h3>
<p class="text-justify">
                       The BA (Hons.) in Economics program started in the year 2023-24 as per the guidelines of NEP 2020. In this program department offers skill based course with strong research orientation. Students learn employment skills of economics through research internship and courses of applied statistics, mathematics and econometrics. Students can opt for discipline specific elective subjects, open elective subjects from different institutes of the University. In addition, this program is uniquely designed to increase the employability and to prepare students to work in a Multi-disciplinary work environment. The program offers Major degree in Economics and is open to students opting for minor specializations as per their interests. Pedagogies concentrating on student’s active participation are extensively used in the teaching-learning process.

