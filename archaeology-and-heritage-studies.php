<p><h3>Program Outline</h3>
<p class="textCSS">Students can study history and archaeology in a scientific manner by enrolling in a one- year certificate program. Via advanced coursework and study, the program will focus on expanding students' knowledge and comprehension of historical eras, cultures, and civilizations. It prepares students for employment in academia, research, cultural heritage management, public history, and allied fields by giving them superior knowledge and abilities in these domains. A significant component of this program will include field trips and hands-on instruction. Our aim is to equip students with the necessary intellectual background and practical abilities to either pursue doctoral studies or seek careers as museum curators, heritage consultants, educators, or academic researchers.</p></div>



