<h2>About Us</h2>
<p>The School of Basic and Applied Sciences (SBAS) department was established in the year 2021. It offers various subjects for study at college/university level, such as Physics, Chemistry, Mathematics, Statistics, Geology, Home Science, Cosmetic Technology and Forensic Science. This department offers programmes at UG level (B.Sc.), PG level (M.Sc.) and Research level (Ph.D.) along with some certificate and diploma courses. These programmes are designed and developed as per the New Education Policy-2020(NEP-2020) and as per the rules and regulations of Government of Maharashtra and UGC, New Delhi.
<br>MGM University is a fast-growing multi-institutional umbrella organization offering multidisciplinary support to SBAS programmes, by supplementing them with requisite inputs from various Engineering, Pharmacy, Biotechnology, Performing Arts, Journalism, Hotel Management, Fine Arts, etc. departments. This makes MGM University to be the only organization in this region that is eminently placed to meet the goals of the NEP-2020, satisfactorily.
<br>The faculty members of SBAS are highly qualified, dedicated and devoted. The laboratories are well equipped and every student will get an opportunity to perform experiments independently in the laboratory. The students are encouraged to interact with faculty members to discuss their problems. Guest lecturers of eminent scientists are arranged regularly. Conferences, workshops and webinars are also arranged frequently for the benefit of the students and to motivate them for grasping the essence of education in science, basic and applied. Besides, attention is directed to every student to improve his/her ability, personality and all-round development to meet the future challenges and to stand on their own feet.
<br>Our aim is to provide high quality basic and fundamental science education and training in research to all aspiring students who wish to make a career as academicians and research scientists and help the nation to face the challenges of sustainable development. 

</p>
