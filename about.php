<h3>About Institute</h3>
<p class="justify-content-center">The Institute of Social Sciences at MGM University was established at MGM University with a vision to prepare students for a career in the development sector. It enhances their understanding of and sensitivity towards different socio-economic and psychological problems and also enables them to address the issues with various social intervention techniques. Empowered with sound theoretical knowledge and robust practical experience, the students not only create a brighter future for themselves but also for society.</br>
The Institute offers UG, PG, and Ph. D. programs in a variety of social sciences including Economics, Psychology,  Social Work, Public Health, Political Science and Governance, and History and Archaeology.  The program curriculum is a blend of important theoretical concepts and practical training that ensures in-depth understanding of the subjects. The highly qualified and experienced faculty of the institute hand-hold students at every step of their way on the academic success.</p>
