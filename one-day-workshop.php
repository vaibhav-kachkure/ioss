<p class="textCSS">Department of Social work had organised a one day workshop on June 22, 2024(Saturday ) at 11.00 am to 4.00 pm on, Research Methodology for Social Work and Public Health Practitioners. The resource person for this workshop was Dr.Girija Ranasingh Thakur, a state level Tuberculosis officer in health department Govt.of Maharashtra. She conducted this workshop on the introductory part of research as well as methodology used in the process of research. She also covered some major highlights in research such as- How to select any topic for research, types of methods, analysis and conclusion. She also took research based simulation game for the students.
<br>The Workshop was inaugurated by the auspicious hands of Dr.Zartab Ansari, Director IOSS. Dr.Amruta Magar, Assistant Professor at Department of Psychology was the Guest of Honour. Ms.Saloni Vaidya did the anchoring, Rishikesh Patil gave the Introduction of the Guest and Ajay Lokhande gave the Vote of Thanks. Dr.Avinash Gore gave the introduction of the program/workshop.
