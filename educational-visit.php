<p class="textCSS">Department of Psychology, B.A Hons.Psychology &amp; M.A clinical psychology (IOSS)
MGMU Visited to Arambh Autisum center on 21 st 2024 August for the awareness
and to gain practical exposure to autism spectrum disorder (ASD) and learn about
therapeutic interventions used at the center.The Arambh Autism Center is a
specialized institution dedicated to the care and development of children and
individuals diagnosed with Autism Spectrum Disorder (ASD). The primary objective
of the visit was to assess the psychological interventions, behavioral management
strategies, and educational approaches implemented at the center. Additionally, the
focus was on understanding how the center supports both the emotional and cognitive
needs of autistic individuals and their families. Students gained their experience and
knowledge with the Speech Therapy Session: Observing how children with ASD
work on language development and communication skills. Occupational Therapy
Session: Understanding techniques used to improve fine motor skills, sensory
processing, and daily living activities. Behavioral Intervention: Viewing methods
used to manage behavioral issues and develop adaptive skills.<br> Dr. Pratibha Nila, Dr. Amruta Magar, Dr. Amrapali Jogdand, Dr Aslam Malik &amp;
